	mov	si, WIT_LAYER_ID
	call	WinGetInfo
	mov	dx, ax			; return in dx
	mov	si, WIT_PRIORITY	; get WinPriorityData
	call	WinGetInfo		; return in al
	pop	di

	.leave
	ret
