COMMENT @----------------------------------------------------------------------

ROUTINE:	UpdateConstrainState

SYNOPSIS:	Updates status of UIFA_CONSTRAIN

CALLED BY:	FlowKbd

PASS:		*ds:si	- Instance data
		cx	- charValue
		dx	- low byte CharFlags, high byte ShiftState
		bp	- low byte toggleState, high byte scanCode

