RestorePriorities	proc	near
	tst	bx			; if no geode, done
	jz	done

	push	bx
	tst	al
	jz	doneWithFirst
	call	ProcInfo		; get first thread
	tst	bx
	jz	doneWithFirst
	mov	ah, mask TMF_BASE_PRIO
	call	ThreadModify
doneWithFirst:
	pop	bx

	push	bx
	tst	cl
	jz	doneWithUI
	push	ds
	call	MemLock
	mov	ds, ax
	mov	ax, ds:[PH_uiThread]
	call	MemUnlock
	pop	ds
	mov	bx, ax
	tst	bx
	jz	doneWithUI
	mov	al, cl			; get prio to restore
	mov	ah, mask TMF_BASE_PRIO
	call	ThreadModify
doneWithUI:
	pop	bx

done:
	ret
RestorePriorities	endp


